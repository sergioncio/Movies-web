function mostrarformfoto(){
    var formcambiarfoto=document.getElementById('formcambiarfoto');
    var enlacecambiarfoto=document.getElementById('enlacecambiarfoto');
    formcambiarfoto.style.visibility='visible';
    formcambiarfoto.style.display='flex';
    enlacecambiarfoto.style.visibility='hidden';
    enlacecambiarfoto.style.display='none';
    return true;
}

